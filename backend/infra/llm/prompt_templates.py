def render_template(template: str, **variables) -> str:
    """渲染提示词模板，替换 {{var}} 占位符。"""
    result = template
    for key, value in variables.items():
        placeholder = "{{" + key + "}}"
        result = result.replace(placeholder, str(value) if value is not None else "")
    return result


